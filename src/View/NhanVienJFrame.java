    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        int row = tblNhanVien.getSelectedRow();
        if(row == -1){
            return;
        }
        
        if(Optional.ofNullable(tblNhanVien.getValueAt(row, 4)).isEmpty()){
            JOptionPane.showMessageDialog(this, "Không được null");
            return;
        }
        
        String MaNV = tblNhanVien.getValueAt(row, 1).toString();
        String TenNV = tblNhanVien.getValueAt(row, 2).toString();
        String GioiTinh = tblNhanVien.getValueAt(row, 3).toString();
        String NgaySinh = tblNhanVien.getValueAt(row, 4).toString();
        String CCCD = tblNhanVien.getValueAt(row, 5).toString();
        String DiaChi = tblNhanVien.getValueAt(row, 6).toString();
        String SDT = tblNhanVien.getValueAt(row, 7).toString();
        String Email = tblNhanVien.getValueAt(row, 8).toString();
        String TrangThai = tblNhanVien.getValueAt(row, 9).toString();
        
        txtMaNV.setText(MaNV);
        txtTenNV.setText(TenNV);
        if(GioiTinh.equals("Nam")){
            this.rdoNam.setSelected(true);
        }else{
            this.rdoNu.setSelected(true);
        }
        txtNgaySinh.setText(NgaySinh);
        txtCCCD.setText(CCCD);
        txtDiaChi.setText(DiaChi);
        txtSDT.setText(SDT);
        txtEmail.setText(Email);
        if(TrangThai.equals("Đang làm")){
            this.rdoDangLam.setSelected(true);
        }else{
            this.rdoDaNghi.setSelected(true);
        }                             
    }//GEN-LAST:event_tblNhanVienMouseClicked

