    public Date NgaySinh(String ngaysinh) {

        SimpleDateFormat sdf = new SimpleDateFormat();
        sdf.applyPattern("yyyy-MM-dd");
        Date d = null;
        try {
            d = sdf.parse(ngaysinh);
        } catch (ParseException ex) {
            ex.getMessage();
        }

        return d;
    }
    
    
