    private void LoadTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblNhanVien.getModel();
        dtm.setRowCount(0);
        
        ArrayList<NhanVienVM> listNVVM = nhanVienService.getAll();
        
        for(NhanVienVM nvvm : listNVVM){
            dtm.addRow(new Object[]{
                nvvm.getId(),
                nvvm.getMaNV(),
                nvvm.getTenNV(),
                nvvm.getGioiTinh()== 1 ? "Nam" : "Nữ",
                nvvm.getNgaySinh(),
                nvvm.getCCCD(),
                nvvm.getDiaChi(),
                nvvm.getSDT(),
                nvvm.getEmail(),
                nvvm.getTrangThai()== 1 ? "Đang làm" : "Đã nghỉ",
            });
        }
    }
    
