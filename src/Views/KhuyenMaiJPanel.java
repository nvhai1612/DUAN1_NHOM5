    private void tblKhuyenMaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhuyenMaiMouseClicked
                            
        int row = tblKhuyenMai.getSelectedRow();
        if (row == -1) {
            return;
        }
        txtTenChuongTrinh.setText(tblKhuyenMai.getValueAt(row, 3).toString());
        txtMucGiaGiam.setText(tblKhuyenMai.getValueAt(row, 4).toString());
        txtThoiGianBatDau.setText(tblKhuyenMai.getValueAt(row, 5).toString());
        txtThoiGianKetThuc.setText(tblKhuyenMai.getValueAt(row, 6).toString());

        String t = tblKhuyenMai.getValueAt(row, 7).toString();

        if (t.equals("Yes")) {
            rdoDangHoatDong.setSelected(true);
        } else {
            rdoNgungHoatDong.setSelected(true);

        }
    }//GEN-LAST:event_tblKhuyenMaiMouseClicked

