    private void LoadTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblKhuyenMai.getModel();
        dtm.setRowCount(0);
        ArrayList<khuyeMaiVM> listSPVM = seviec.getAll();

        for (khuyeMaiVM x : listSPVM) {
            dtm.addRow(new Object[]{
                x.getIdKM(),
                x.getIDSP(),
                x.getMaKM(),
                x.getTenKM(),
                x.getMucGiamGia(),
                x.getThoiGianBatDau(),
                x.getThoiGianKetThuc(),
                x.getTrangThai() == 1 ? "Hoạt động" : "Dừng Hoạt động",
                x.getSoLuong()
            });
        }
    }

    private void LoadTablesp() {
        DefaultTableModel dtm = (DefaultTableModel) tblSanPham.getModel();
        dtm.setRowCount(0);
        ArrayList<SanPhamVM> spvm = service2.getAll();

        for (SanPhamVM x : spvm) {
            dtm.addRow(new Object[]{
                x.getId(),
                x.getMaSP(),
                x.getTenSP(),
                x.getTrangThaiSP() == 1 ? "Hoạt động" : "Dừng Hoạt động"
            });
        }

    }

